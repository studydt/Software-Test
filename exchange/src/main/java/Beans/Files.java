package Beans;

public class Files {
    private String[] FilesName;
    private String in_package;
    private String out_package;

    public String getIn_package() {
        return in_package;
    }

    public void setIn_package(String in_package) {
        this.in_package = in_package;
    }

    public String getOut_package() {
        return out_package;
    }

    public void setOut_package(String out_package) {
        this.out_package = out_package;
    }

    public String[] getFilesName() {
        return FilesName;
    }

    public void setFilesName(String[] filesName) {
        FilesName = filesName;
    }
}
